     */
    public function index()
    {
        $user = Auth::user();
    	
  		$personaggio = Personaggio::find($user->personaggio_id);
  		
  		$now = Carbon::now();
  		$age = $now->year - $personaggio->birthdate;
  		$birthdate = Carbon::parse($personaggio->birthdate);
  		$birthdate_formatted = $birthdate->formatLocalized('%d %B %Y');
  		
  		return view('character.card', compact('user', 'personaggio', 'age', 'birthdate_formatted'));
    }

