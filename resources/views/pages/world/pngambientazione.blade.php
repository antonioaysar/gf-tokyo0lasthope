In questa categoria rientrano sia i PnG di conoscenza comune di tutti i cittadini di Tokyo data la loro importanza all'interno della società, sia le figure già create e che possono essere utili ai fini del gioco e che possono essere conosciuti da una categoria di personaggi.<br><br>

<b>Hisoka Meiji e Ailen Meiji » Imperatore e Imperatrice di Tokyo</b><br><br>

Sposati dal 14 gennaio 316. Come da tradizione si sono uniti in matrimonio nel primo mese dell'anno come buon augurio alla propria discendenza. Attualmente non hanno figli.<br>
<b>Hisoka</b><br>
Nato il 20 dicembre, periodo che viene considerato grande festa in tutta la città in quanto Natale e in onore al regnante.<br>
Discendente della Luna. 34 anni. Originario della Corea.<br>
<b>Ailen</b><br>
Nata il 16 aprile, giorno che viene considerato festivo per onorarla.<br>
Discendente della Terra. 32 anni. Originaria dell'Argentina.<br>
Conosciuti da: <u>chiunque</u><br>
