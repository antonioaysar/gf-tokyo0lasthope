<center><div class=titoli>Cos'è una Dinastia</div></center>
<div class=block>
All'interno di Tokyo 0: Last Hope è possibile accedere ad un tipo di gioco più articolato e complesso che si sviluppa attraverso le 5 Dinastie. Esse sono il corrispondente di "gilda" o "corporazione" di altre Land, con un termine riadattato all'ambientazione e alla storia che ha valenza ongame. Il che vuol dire che "Dinastia" non è solo un modo offgame per riferirsi alla stessa ma anche ongame.<br>
La Dinastia è un insieme di personaggi e di regole che compongono un gruppo finalizzato al raggiungimento dello stesso obiettivo. Fanno parte di questa fazione personaggi con un fine in comune e che si aiuteranno l'un l'altro pur di raggiungerlo. <br>
Più nello specifico, addentrandoci nell'universo di Tokyo 0: Last Hope, ogni Dinastia corrisponde alle fazioni che esistevano durante il Silver Millennium o che sono state create proprio durante lo stesso: coloro che combattono per il bene, coloro che sono corrotti dal male, coloro che dedicano la propria esistenza a non appoggiare nessuna delle due cause. Il gioco che ogni Dinastia offre è dunque variabile, intenso e pieno di sfaccettature. Scegliere una Dinastia è libera scelta del giocatore ed è suo compito far in modo che il personaggio sia incline alla stessa, nonché rispettarne i requisiti di ingresso.

</div>

