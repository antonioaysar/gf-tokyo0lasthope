	<div class=rulebook-content-title>Regolamento Bacheche</div>
</center>

<div class=block>
	All'interno di Last Hope ci sono vari spazi che permettono agli utenti di interagire offgame con altri utenti o di permettere una rapida comunicazione: le bacheche.<br>
	Ogni sezione è dedicata ad un uso specifico e tale deve rimanere. Si fa inoltre presente che è obbligatorio mantenere buona educazione e rispetto reciproco.
</div>

<center>
	<div class=rulebook-content-title>Organizzazione Gioco - Offgame</div>
</center>

<div class="block">
	<b>- Organizzazione Quest ›</b><br>
	Bacheca ad uso dei Master per l'organizzazione e proposte di Eventi e/o Quest costruite per l'utenza. <u>E' vietato lo spam. </u><br><br>
	
	<b>- Dinastia ›</b><br>
	Bacheca <i>riservata solo a tutti gli appartenenti alla stessa Dinastia</i> per l'organizzazione del gioco interno, comunicazioni tra Capodinastia e appartenenti, e ulteriori informazioni/promemoria legati alla Dinastia.<br><br>
	
	<b>- Mestiere ›<br></b>
	Bacheca <i>riservata solo a tutti gli appartenenti  allo stesso Mestiere</i> per l'organizzazione del gioco interno, comunicazioni tra Capomestiere e appartenenti, e ulteriori informazioni/promemoria legati al Mestiere.<br>
 </div>

<center>
<div class=rulebook-content-title>Gioco - Ongame</div></center>
<div class=block>
 <b>- Tokyo News ›<br></b>
Bacheca di informazione ongame che può essere usata dagli appartenenti al Mestiere. I Master sono autorizzati solo all'occorrenza in mancanza dei mestierati. Tutti i Pg giocanti possono usufruire delle info ongame messe in questa bacheca.<br><br>

 <b>- TokyoNet ›<br></b>
Bacheca ongame al pari di web, social network e forum. Rappresenta tutta la rete online che ogni personaggio può sfruttare per qualsiasi motivo a sua discrezione. <br><br>

<b>- Tokyo Nightlife ›<br></b>
Bacheca ongame per la pubblicizzazione di eventi mondani organizzati in città. Sono autorizzati all'uso di questa bacheca gli appartenenti al mestiere di spettacolo&arte e i Master.<br><br>

</div>
<center><div class=rulebook-content-title>Informazioni e Altro. Offgame.</div></center>
<div class=block>
<b>- Comunicazioni & Novità Staff ›</b><br>
Bacheca ad <u>esclusivo uso dello Staff</u>. E' vietato introdurre messaggi in questa sezione se non si fa parte dello Staff. Master, Moderatori e Guide devono essere autorizzati dagli Admin.<br>
Scopo della bacheca: comunicare all'utenza implementazioni e/o decisioni/avvisi da parte dello Staff. <u>E' vietato lo spam. </u><br><br>

<b>- Richieste&Info Dinastie ›</b><br>
Bacheca ad uso esclusivo dei Capidinastia: occorre per raccogliere le richieste inviate off-game a ciascuna di tali divisioni e mantenere ordinati / monitorati gli ingressi.<br><br>

<b>- Richieste&Info Mestieri ›</b><br>
Bacheca ad uso esclusivo dei Capimestieri: occorre per raccogliere le richieste inviate off-game a ciascuna di tali divisioni e mantenere ordinati / monitorati gli ingressi. <br><br>

<b>- Benvenuti & Assenze ›</b><br>
Bacheca ad uso di tutti i neo iscritti che desiderano (ma non è obbligatorio) presentarsi alla comunità ludica.<br>
Bacheca ad uso di tutti coloro che necessitano di dover avvisare la propria assenza dalla Land per più di 5 giorni. <u>E' vietato lo spam. </u><br><br>

<b>- Free  ›</b><br>
Bacheca ad uso di tutti gli utenti per qualsiasi argomento di svago inerente alla land o meno.<br>
• E' severamente vietato postare argomenti offensivi verso terzi<br>
• E' severamente vietato abusare di questo spazio, per tanto si invia a farne un uso moderato e non continuo<br>
• E' severamente vietato usare questo spazio per la pubblicità di qualsiasi genere di materiale<br>
• E' severamente vietato questo spazio per innescare qualsiasi genere di discussione di natura privata tra uno o più Player<br><br>

<b>- Bug & Proposte ›</b><br>
Bacheca ad uso di tutti gli utenti per comunicare e/o segnalare eventuali bug riscontrati durante il gioco.<br>
Bacheca ad uso di tutti gli utenti per esporre eventuali proposte per migliorare il gioco comune o la funzionalità del sito.<br>
• Lo Staff è tenuto a prendere in considerazione ogni proposta ma non è obbligato ad esaudirla purché fornisca un'adeguata spiegazione in merito.<br>
<u>E' vietato lo spam.</u><br><br>

<b>- F.A.Q. ›<br></b>
Bacheca ad uso di qualsiasi utente che ha dubbi e domande da esporre su regolamenti, dinastie, mestieri, funzionalità. <u>E' vietato lo spam</u>.<br>
</div>

