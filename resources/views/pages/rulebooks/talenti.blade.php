<b>Lingue</b><br>
1 punto corrisponde ad una laurea non obbligatoria. Potete avere le conoscenze non necessariamente legate ad una laurea. Non è possibile prendere più di due campi di specializzazione, ma nello stesso settore (ad esempio Scientifico) è possibile con il tempo acquisire più lauree. Esempio: Medicina + Fisica + Matematica + Lettere.<br>
Ma non Medicina + Lettere + Legge.<br><br>

