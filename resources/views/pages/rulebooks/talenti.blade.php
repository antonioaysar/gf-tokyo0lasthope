<b>Sopravvivenza</b><br>
per sopravvivenza si intende la capacità di adattarsi ad un ambiente ostile o di riuscire a cavarsela in situazioni di disagio e difficoltà intese come ambiente circostante.<br>
• Costruzione di rifugi di fortuna;<br>
• Accendere il fuoco senza oggetti automatici;<br>
• Cacciare;<br>
• Seguire tracce;<br>
• Trattare cibo;<br><br>

<b>Sport</b><br>
E' possibile scegliere un solo sport a livello agonistico e 2 a livello amatoriale; si raggiunge l'abilità agonistica al 3 grado di questo Talento.
