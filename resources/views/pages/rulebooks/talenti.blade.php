<center><div class=rulebook-content-title>Talenti: cosa sono?</div></center>
<div class=block><br><br>

Una volta creato il proprio personaggio ed effettuato il login in Land, aprendo la propria scheda è possibile completare la fase di creazione smistando i punti a disposizione per scegliere i talenti del proprio personaggio e personalizzarlo maggiormente. L'assegnazione è automatica e il dispendio di punti aumenta con l'aumento del livello di capacità. Nel corso del gioco è possibile incrementare i talenti o acquisirne di nuovi.
Nel caso di Talenti di combattimento, i bonus devono essere considerati su un calcolo dei danni e non ai tiri per colpire.<br><br>

<i>Livelli di capacità</i><br>
 minimo 0 massimo 5 per ogni talento che si vuole acquisire.<br><br>

<u>tabella livelli di capacità</u>
0. il livello 0 non equivale all'incapacità totale di un personaggio ma ad una caratteristica umana e limitata (esempio: non avere il talento informatica vi concede comunque di saper usare telefoni e pc, non avere il talento cucina vi consente comunque di saper fare un caffé o un piatto di pasta)<br>
1 › base<br>
2 › medio<br>
3 › molto capace<br>
4 › esperto<br>
5 › eccellente<br><br>

Per ogni Talento esistono delle sottocategorie per cui non occorre spendere punti aggiuntivi per averle ma che servono per darvi maggior scelta precisa e per spiegare meglio cosa comporta avere o non avere il talento.<br><br>

