<center><div class=rulebook-content-title>Livelli di Potere</div></center>
<div class=block>
I Livelli di potere corrispondono all'abilità ed alla forza maturata dal vostro personaggio all'interno del gioco in ambito magico.
Questi possono essere raggiunti mediante il raggiungimento di un determinato quantitativo di punti esperienza, che vengono alzati tramite Quest o tramite Free Role.<br>
Ogni volta che si raggiunge il livello corrispettivo di esperienza necessario per raggiungere il livello successivo, vengono messi a disposizione del giocatore <b>10 punti distribuibili</b> a piacimento sulle statistiche del vostro personaggio.<br><br>

Una volta raggiunta la soglia dell'esperienza, comunicate il vostro avanzamento di livello e la distribuzione dei punti nelle varie statistiche secondo le vostre preferenze, al referente della vostra Dinastia o, nel caso questo fosse assente, ad un membro dello Staff.<br><br>

Qui di seguito vi mettiamo lo schema del raggiungimento dei vari livelli.<br>
Ogni personaggio - da umano - parte dal livello 0.<br><br>

20 - lv1 +10 punti<br>
60 - lv2 +10 punti<br>
120 – lv3 +10 punti<br>
220 - lv4 +10 punti<br>
400 - lv5 +10 punti <br>
</div>

<center><div class=rulebook-content-title>Avanzamento di grado in Dinastia </div></center>
<div class=block>
Si alzano tramite la meritocrazia del singolo, per i successi in gioco, l'affidabilità del singolo o l'impegno nel perseguire gli obiettivi di Dinastia.<br><br>
Esiste un tempo minimo che viene vissuto ON ed OFF per giocatore e personaggio utile per valutare la costanza, l'impegno e per sviluppare meccaniche di gioco variabili e dipendenti sia dal personaggio che dal gruppo di appartenenza, oltre che dalle situazioni e dall'abilità del giocatore nel creare gioco.<br>
Ad ogni passaggio di grado vi è un riconoscimento in ON che varierà a seconda della Dinastia di appartenenza tranne il primo step in cui si verrà solo instradati all'interno del gioco di una delle quattro macro-aree che caratterizzano il tipo di gioco desiderato ed adatto ai diversi personaggi.<br><br>

Fermo restando questo tempo minimo, i Capi Dinastia sono gli incaricati delle valutazioni dell'idoneità di un personaggio se passare o meno allo step successivo. Non è possibile quindi avanzare prima del tempo minimo qui sotto definito e si invita l'utenza alla coerenza e di non domandare continuamente il momento in cui arriverà il prossimo avanzamento di livello.
Altresì si invitano Tutti i Capi Dinastia a motivare all'utente in esame ed alla gestione la causa del ritardo nell'avanzare di Livello, onde evitare spiacevoli disguidi.<br><br>

• Grado 1 > Grado 2: 1 mese <br>
• Grado 2 > Grado 3: 2 mesi <br>
• Grado 3 > Grado 4: 3 mesi<br>
• Grado 4 Grado 5: 4 mesi <br>

<br></div>

<center><div class=rulebook-content-title>Avanzamento di grado nei Mestieri</div></center>
<div class=block>
Si alzano tramite la meritocrazia del singolo, per i successi in gioco, l'affidabilità del singolo o l'impegno nel perseguire gli obiettivi del Mestiere oltre che all'esperienza nel proprio lavoro.<br<
Similmente alle Dinastie, esiste un tempo minimo che viene vissuto ON ed OFF per giocatore e personaggio utile per valutare la costanza, l'impegno e per sviluppare meccaniche di gioco variabili e dipendenti sia dal personaggio che dal gruppo di appartenenza, oltre che dalle situazioni e dall'abilità del giocatore nel creare gioco.<br><br>

Fermo restando questo tempo minimo, i Capi Mestiere sono gli incaricati delle valutazioni dell'idoneità di un personaggio se passare o meno allo step successivo. Non è possibile quindi avanzare prima del tempo minimo qui sotto definito e si invita l'utenza alla coerenza e di non domandare continuamente il momento in cui arriverà il prossimo avanzamento di livello.
Altresì si invitano Tutti i Capi Mestiere a motivare all'utente in esame ed alla gestione la causa del ritardo nell'avanzare di Livello, onde evitare spiacevoli disguidi. <br><br>

- Il passaggio può avvenire ogni mese. <br>
NB: le maggiori specifiche sono inserite negli statuti di mestiere corrispondenti con le dovute eccezioni motivate dal gioco ongame.

