<center>
	<div class=rulebook-content-title>Chi sono i PnG?</div>
</center>

<div class=block>
	Il termine PnG indica un personaggio non mosso da un player e che quindi esiste in automatico all'interno del gioco. L'esempio migliore dei PnG può essere dato con il paragone di una comparsa all'interno di un film: immaginate i vostri Pg come i protagonisti e i Png come il contorno che dà movimento e vita alla scena. Mentre il vostro Personaggio viene mosso nelle vostre azioni attorno a lui ci sarà chiunque: passanti, lavoratori, altri che vivono contemporaneamente indipendentemente dal gioco dei singoli Player. Non sono visibili, non hanno una scheda, non hanno uno scopo: sono la quotidianità che vi circonda. 
	<br><br>

	<center>
		<div class=rulebook-content-title>Come si può interagire con i Png? </div>
	</center>

	E' importante sapere che si può interagire e anzi si deve con i PnG, ma con moderazione. Se per esempio stiamo giocando all'interno di un bar, bisogna tener conto della presenza del personale della suddetta attività che, a meno che non sia dichiarata da un Pg, è tutta costituita da PnG. <br><br>

	<i>Esempio: </i><br>

	<b>1.</b> Karl entra nel bar, si guarda attorno e decide di avvicinarsi al bancone per ordinare qualcosa da bere: si rivolge quindi all'uomo dall'altra parte comunicandogli quanto desidera. < Grazie! > Replica poi non appena avrà avuto la sua bevanda, sorridendo e prendendo il bicchiere.  <b>SI </b>
	<br><br>
<i>Esempio: </i><br>

<b>2.</b> Karl entra nel bar, si guarda attorno e decide di avvicinarsi al bancone per ordinare qualcosa da bere: si rivolge quindi all'uomo dall'altra parte comunicandogli quanto desidera. < Grazie! > Replica poi non appena avrà avuto la sua bevanda, sorridendo e prendendo il bicchiere. Il barista poi oltrepassa il bancone, si avvicina a Karl e gli lancia un pugno. <b>NO </b>
<br><br>

<i>Bisogna ricordare che </i><br>

<br>• E' vietato arrecare danni ai Png senza un Master presente o senza un corretto utilizzo di poteri/dadi 
<br>• E' vietato farsi arrecare danni dai Png senza un Master presente 
<br>• E' possibile creare Png legati al proprio personaggio (amici, parenti, colleghi etc.) ma senza usarli all'attivo e senza abusare della loro funzione di contorno e sfondo 
<br>• E' vietato usarli per informazioni inerenti alla trama senza una presenza Master a condurli 
<br>
<br>Queste regole non valgono per i Master: essi sono gli unici a poter muovere i Png come meglio credono, secondo le regole della Land, per creare situazioni di interazione con gli altri Personaggi e per gli sviluppi di trama. 
</div>